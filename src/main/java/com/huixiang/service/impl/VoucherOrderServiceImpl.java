    private class VoucherOrderHandler implements Runnable {
        @Override
        public void run() {
            while (!Thread.currentThread().isInterrupted()) {
                try {
                    List<MapRecord<String, Object, Object>> list = stringRedisTemplate.opsForStream().read(
                            Consumer.from("g1", "c1"),
                            StreamReadOptions.empty().count(1).block(Duration.ofSeconds(2)),
                            StreamOffset.create("stream.orders", ReadOffset.lastConsumed())
                    );
                    if (list == null || list.isEmpty()) {
                        continue;
                    }
                    MapRecord<String, Object, Object> record = list.get(0);
                    VoucherOrder voucherOrder = BeanUtil.fillBeanWithMap(record.getValue(), new VoucherOrder(), true);
                    voucherOrder.setRequestId(generateRequestId());
                    Long userId = voucherOrder.getUserId();
                    ExecutorService executor = isVipUser(userId) ? VIP_ORDER_EXECUTOR : NORMAL_ORDER_EXECUTOR;
                    executor.submit(() -> {
                        try {
                            createVoucherOrder(voucherOrder);
                        } catch (Exception e) {
                            log.error("order processing failed: {}", voucherOrder.getId(), e);
                        }
                    });
                    stringRedisTemplate.opsForStream().acknowledge("stream.orders", "g1", record.getId());
                } catch (Exception e) {
                    if (e instanceof RedisCommandInterruptedException ||
                            e.getCause() instanceof InterruptedException ||
                            Thread.currentThread().isInterrupted()) {
                        log.info("thread interrupted, exiting");
                        break;
                    }
                    log.error("order handler exception", e);
                    handlePendingList();
                }
            }
        }

        private boolean isVipUser(Long userId) {
            try {
                String key = RedisConstants.USER_INFO_KEY + userId + ":level";
                String levelStr = stringRedisTemplate.opsForValue().get(key);
                if (levelStr != null) {
                    return Integer.parseInt(levelStr) > 0;
                }
                return false;
            } catch (Exception e) {
                return false;
            }
        }

        private void handlePendingList() {
            while (!Thread.currentThread().isInterrupted()) {
                try {
                    List<MapRecord<String, Object, Object>> list = stringRedisTemplate.opsForStream().read(
                            Consumer.from("g1", "c1"),
                            StreamReadOptions.empty().count(1),
                            StreamOffset.create("stream.orders", ReadOffset.from("0"))
                    );
                    if (list == null || list.isEmpty()) {
                        break;
                    }
                    MapRecord<String, Object, Object> record = list.get(0);
                    VoucherOrder voucherOrder = BeanUtil.fillBeanWithMap(record.getValue(), new VoucherOrder(), true);
                    voucherOrder.setRequestId(generateRequestId());
                    Long userId = voucherOrder.getUserId();
                    ExecutorService executor = isVipUser(userId) ? VIP_ORDER_EXECUTOR : NORMAL_ORDER_EXECUTOR;
                    executor.submit(() -> {
                        try {
                            createVoucherOrder(voucherOrder);
                        } catch (Exception e) {
                            log.error("pending order processing failed: {}", voucherOrder.getId(), e);
                        }
                    });
                    stringRedisTemplate.opsForStream().acknowledge("stream.orders", "g1", record.getId());
                } catch (Exception e) {
                    log.error("pending list processing error", e);
                }
            }
        }
    }

