    @Override
    public void destroy() throws Exception {
        log.info("shutting down thread pools...");
        isRunning.set(false);
        SECKILL_ORDER_EXECUTOR.shutdownNow();
        VIP_ORDER_EXECUTOR.shutdownNow();
        NORMAL_ORDER_EXECUTOR.shutdownNow();
        DELAY_ORDER_EXECUTOR.shutdownNow();
        try {
            if (!SECKILL_ORDER_EXECUTOR.awaitTermination(5, TimeUnit.SECONDS)) {
                log.warn("seckill order pool did not terminate in 5s");
            }
            if (!VIP_ORDER_EXECUTOR.awaitTermination(5, TimeUnit.SECONDS)) {
                log.warn("VIP order pool did not terminate in 5s");
            }
            if (!NORMAL_ORDER_EXECUTOR.awaitTermination(5, TimeUnit.SECONDS)) {
                log.warn("normal order pool did not terminate in 5s");
            }
            if (!DELAY_ORDER_EXECUTOR.awaitTermination(5, TimeUnit.SECONDS)) {
                log.warn("delay order pool did not terminate in 5s");
            }
        } catch (InterruptedException e) {
            log.error("interrupted while waiting for pools to terminate");
            Thread.currentThread().interrupt();
        }
        log.info("thread pools shut down");
    }

